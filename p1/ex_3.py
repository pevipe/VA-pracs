def show_results(img1, img2, global_title='Test', text1='Original', text2='Modificada'):
    plt.figure(figsize=(12,6))
    plt.title(global_title)

    # Imagen original
    plt.subplot(1,2,1)
    plt.imshow(img1, cmap='gray')
    plt.title(text1)
    plt.axis('off')

    # Histograma original
